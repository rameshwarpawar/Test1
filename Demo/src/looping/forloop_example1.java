package looping;

public class forloop_example1 {
	
	    public static void main(String args[])
	    {
	    	//program to print number 1 to 5  output=> 1 2 3 4 5
	    	//starting point intialiazation 1
	    	// end point condition 5 true 
	    	// increment 
	    	
	    	for (int a=1;a<=5;a++)
	    	{
	    		System.out.print(a);
	    	}
	    }

}
