package looping;

public class looping_example5 {
	//program to print number from 5 to 1 => 5 4 3 2 1
		//starting point   5
		//end point         1
		//decrement operator
	    public static void main(String args[])
	    {
	    	for(int a=5;a>=1;a--)
	    	{
	    		System.out.print(a);
	    	}
	    	
	    //  Program to print in format 5 10 15 20 25 30
			//	5		35<=30  35
	    	
	    	for (int j=5;j<=30;j=j+5)
	    	{
	    		System.out.println(j);
	    	}
	    }
}
