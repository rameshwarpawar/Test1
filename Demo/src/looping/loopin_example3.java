package looping;

public class loopin_example3 {
	
	//Program to print even numbers from 1 to 100
		//starting point  1
		//end point       100
		//increment operator
	  
	         public static void main(String args[])
	         {
	        	 for (int i=1;i<=100;i++)
	        	 {
	        		 if (i%2==0)
	        		 {
	        			 System.out.println(i+" is even number");
	        		 }
	        	 }
	         }
	        		 

}
